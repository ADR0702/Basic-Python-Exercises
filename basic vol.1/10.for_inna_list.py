# "Create a list with 5 favorite animals (e.g., ["dog", "cat", "tiger", "fox", "elephant"]).

# Use a for loop to go through the list and display each animal with the text:
# My favorite animal is [animal]."**

animal=['dog', 'cat', 'tiger', 'fox', 'elephant']

for i in animal:
    print(i)
    print(f"my favorite animal is {i}")